

QUESTION1 = "How are rest and motion defined in physics?"
ANSWER1 = """
An object is said to be at rest when its position does not change with time in relation to its surroundings.
An object is in motion when its position changes with time relative to its surroundings.
"""

QUESTION2 = "What is the definition of work in physics?"
ANSWER2 = """
Work is done when a force is applied to an object, and the object moves in the direction of the applied force.
It is calculated as the product of force and displacement in the direction of the force.
"""

QUESTION3 = "What is thermal expansion, and how does it affect solids, liquids, and gases?"
ANSWER3 = """
Thermal expansion refers to the increase in the size of a substance when its temperature increases.
Solids expand the least, liquids expand more, and gases expand the most when heated.
"""

QUESTION4 = "What is a wave, and how is sound produced?"
ANSWER4 = """
A wave is a disturbance that transfers energy through a medium without transferring matter.
Sound is produced by vibrating objects, and it travels as a longitudinal wave through a medium like air, water, or solids.
"""

QUESTION5 = "What is refraction, and why does it occur?"
ANSWER5 = """
Refraction is the bending of light as it passes from one medium to another with a different density.
It occurs because light changes speed when moving between materials with different refractive indices.
"""

